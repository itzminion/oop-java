class Main{
    public static void main(String args[]){
        int a=22;
        int b=33;
        int c=2;
       
        System.out.println("a="+a);
        System.out.println("b="+b);
        System.out.println("a&b="+(a&b));
        System.out.println("a|b="+(a|b));
        System.out.println("a<<b"+(a<<c));
        System.out.println("a>>b="+(a>>c));
        System.out.println("a^b="+(a^b));
        System.out.println("~a="+(~a));
    }
}